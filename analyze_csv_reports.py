def generate_summary_report():
    """Generate overall summary report."""
    print("\n" + "="*80)
    print("📊 OVERALL TEST SUMMARY REPORT")
    print("="*80)
    
    csv_logger = get_csv_logger()
    
    # Count records in each CSV
    registrations = csv_logger.read_csv("user_registrations")
    orders = csv_logger.read_csv("order_details")
    validations = csv_logger.read_csv("validation_results")
    carts = csv_logger.read_csv("cart_details")
    
    print(f"\n📁 CSV Files Generated:")
    print(f"   - user_registrations.csv: {len(registrations)} records")
    print(f"   - order_details.csv: {len(orders)} records")
    print(f"   - validation_results.csv: {len(validations)} records")
    print(f"   - cart_details.csv: {len(carts)} records")
    
    # Calculate success rates
    if registrations:
        reg_df = pd.DataFrame(registrations)
        reg_success_rate = len(reg_df[reg_df['Validation Result'] == 'Success']) / len(reg_df) * 100
        print(f"\n✅ Registration Success Rate: {reg_success_rate:.1f}%")
    
    if validations:
        val_df = pd.DataFrame(validations)
        val_pass_rate = len(val_df[val_df['Result'] == 'Pass']) / len(val_df) * 100
        print(f"✅ Validation Pass Rate: {val_pass_rate:.1f}%")
    
    print(f"\n📂 CSV files location: reports/csv/")


