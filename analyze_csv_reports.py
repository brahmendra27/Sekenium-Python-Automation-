"""
Analyze CSV Test Reports.

This script demonstrates how to read and analyze CSV test reports
generated by the test automation framework.
"""

from framework.csv_logger import get_csv_logger
from pathlib import Path
import pandas as pd

def analyze_registrations():
    """Analyze user registration data."""
    print("\n" + "="*80)
    print("📊 USER REGISTRATION ANALYSIS")
    print("="*80)
    
    csv_logger = get_csv_logger()
    registrations = csv_logger.read_csv("user_registrations")
    
    if not registrations:
        print("⚠️ No registration data found")
        return
    
    # Convert to DataFrame for easier analysis
    df = pd.DataFrame(registrations)
    
    print(f"\n📈 Total Registrations: {len(df)}")
    print(f"✅ Successful: {len(df[df['Validation Result'] == 'Success'])}")
    print(f"❌ Failed: {len(df[df['Validation Result'] == 'Failed'])}")
    
    if len(df) > 0:
        print(f"\n📧 Recent Registrations:")
        print(df[['Timestamp', 'First Name', 'Last Name', 'Email', 'Validation Result']].tail(5).to_string(index=False))


