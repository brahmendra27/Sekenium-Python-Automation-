def analyze_validations():
    """Analyze validation results."""
    print("\n" + "="*80)
    print("📊 VALIDATION RESULTS ANALYSIS")
    print("="*80)
    
    csv_logger = get_csv_logger()
    validations = csv_logger.read_csv("validation_results")
    
    if not validations:
        print("⚠️ No validation data found")
        return
    
    df = pd.DataFrame(validations)
    
    print(f"\n📈 Total Validations: {len(df)}")
    print(f"✅ Passed: {len(df[df['Result'] == 'Pass'])}")
    print(f"❌ Failed: {len(df[df['Result'] == 'Fail'])}")
    
    if len(df) > 0:
        print(f"\n🔍 Validation Summary by Type:")
        summary = df.groupby(['Validation Type', 'Result']).size().unstack(fill_value=0)
        print(summary)
        
        print(f"\n📋 Recent Validations:")
        print(df[['Timestamp', 'Test Name', 'Validation Type', 'Result']].tail(10).to_string(index=False))
        
        # Show failed validations
        failed = df[df['Result'] == 'Fail']
        if len(failed) > 0:
            print(f"\n❌ Failed Validations:")
            print(failed[['Timestamp', 'Test Name', 'Validation Type', 'Expected Value', 'Actual Value', 'Details']].to_string(index=False))


